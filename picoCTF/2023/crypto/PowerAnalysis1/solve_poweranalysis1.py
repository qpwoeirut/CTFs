# https://github.com/skoppula/aes-sidechannel/blob/c20c0a5e4fb3a997a3ae48a685a232fb332a54bd/cpa/cpa.py

def corr(x, y):
    assert x.shape[0] == y.shape[0], 'Matrix row count mismatch'

    x_std = np.std(x, axis=0)
    y_std = np.std(y, axis=0)
    assert x_std.any() != 0, 'x std is 0'
    assert y_std.any() != 0, 'y std is 0'
    x = x / x_std
    y = y / y_std

    xx = x - np.mean(x, axis=0)
    yy = y - np.mean(y, axis=0)

    # calculate standardized values for each x & y
    xt = np.transpose(xx)
    cc_scale = xt.dot(yy)

    # divide by # of traces to account for division in standard deviation
    cc = np.divide(cc_scale, x.shape[0])
    assert x.shape[0] != 0, 'x.shape[0] is 0'
    return cc


def hw(xx): return bin(xx).count('1')


def sbox_power(p, k): return hw(sbox[(p ^ k) % 256])


def run_cpa(traces, plaintext) -> tuple[list[list[int]], list[np.ndarray]]:
    num_traces = traces.shape[0]

    key_opts = []
    correlations = []
    # Correlation Matrix [16 x (256 x trace_length)]
    for byte in range(16):
        print('Started analyzing key byte %d' % byte)

        # Power hypothesis matrix [num_traces x 256]
        power_hypothesis = np.zeros((num_traces, 256))
        for k in range(256):
            # XOR the plaintext byte with key byte and put the result through the S-BOX
            pwr_model = np.vectorize(sbox_power)
            power_hypothesis[:, k] = pwr_model(plaintext[:, byte].astype(int), k)

        print('Calculating correlation')
        correlation = np.abs(corr(power_hypothesis, traces))

        # --> do some operations here to find the correct byte of the key <--
        correlation = correlation[:, :400]
        max_samples = np.max(correlation, axis=1)  # index of maximum point in sample for each key
        max_key = np.argmax(max_samples)  # index of maximum key among maximums in samples
        max_4_keys = np.argsort(max_samples)[::-1][:4]
        print('max keys:   ', max_4_keys)
        print("correlation:", [max_samples[k].round(3) for k in max_4_keys])
        # print '\t',sorted([(value, i) for i,value in enumerate(max_samples)])

        opts = []
        for key in max_4_keys:
            if max_samples[max_key] - max_samples[key] <= 0.05:
                opts.append(key)

        key_opts.append(opts)
        correlations.append(correlation)

    return key_opts, correlations



