TRACES = 500


def read_input() -> tuple[np.ndarray, np.ndarray]:
    traces = []
    plaintexts = []
    for i in range(TRACES):
        print("Receiving trace", i)
        plaintext = random.randbytes(16)
        r = remote("saturn.picoctf.net", 65035)
        r.recvuntil(b"as hex: ")
        r.sendline(plaintext.hex().encode())

        plaintexts.append(np.array(list(plaintext)))

        trace = ast.literal_eval(r.recvall().split(b':')[1].strip().decode())
        traces.append(trace)

    traces, plaintexts = np.array(traces), np.array(plaintexts)
    return traces, plaintexts


